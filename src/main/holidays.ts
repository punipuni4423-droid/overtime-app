export interface NonWorkingDayResult {
    isNonWorking: boolean
    reason: string
}

/**
 * 土日祝日チェック
 * @param dateStr YYYY-MM-DD 形式
 */
export function checkNonWorkingDay(dateStr: string): NonWorkingDayResult {
    const normalized = dateStr.slice(0, 10)
    const holiday = JAPANESE_HOLIDAYS[normalized]
    if (holiday) return { isNonWorking: true, reason: `祝日（${holiday}）` }

    // UTC解釈を避けるためローカル時刻でパース
    const [y, m, d] = normalized.split('-').map(Number)
    const date = new Date(y, m - 1, d)
    const dow = date.getDay()
    if (dow === 0) return { isNonWorking: true, reason: '日曜日' }
    if (dow === 6) return { isNonWorking: true, reason: '土曜日' }

    return { isNonWorking: false, reason: '' }
}
